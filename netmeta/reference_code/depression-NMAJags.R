# Make R packages R2jags and NMAJags available
#
library(R2jags)
library(NMAJags)


# Standard Bayesian random effects NMA model (with Placebo / standard
# care as reference treatment, i.e., t = 23.
#
# Transform the data into a list suitable for JAGS analysis
#
NMAdataBinary <- make.jagsNMA.data(studyid = dataLinde$studyid,
                                   r = dataLinde$r, n = dataLinde$n,
                                   y = NA, sd = NA, t = dataLinde$t,
                                   type = "binary", data = dataLinde, 
                                   reference = 23)


# Run Jags and create a jags object (run 100.000 iterations and 10.000
# burn-in) monitor the nodes ORref and tau.sq
#
NMAinJAGS <- jags(data = NMAdataBinary, inits = NULL,
                  parameters.to.save = c("ORref", "tau.sq"),
                  n.chains = 2, n.iter = 100000,
                  n.burnin = 10000, DIC = F,
                  model.file = modelNMABinary)

# Print results
#
print(NMAinJAGS, digits = 2)
